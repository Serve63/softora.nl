const {
  buildServerAppOpsWiringContext,
} = require('./server-app-runtime-composition-options');

function buildServerAppOpsWiringRuntimeContext({
  app,
  env,
  envConfig,
  bootstrapState,
  runtimeMemory,
  platformRuntime,
  securityRuntime,
  runtimeSyncRuntime,
  uiSeoRuntime,
  routeManifest,
  appVersion,
  featureFlags,
  getPublicFeatureFlags,
  isServerlessRuntime,
  projectRootDir,
  websiteLinkCoordinator,
  getEffectivePublicBaseUrl,
  resolveLegacyPrettyPageRedirect,
  toPrettyPagePathFromHtmlFile,
  isSmtpMailConfigured,
  isImapMailConfigured,
  upsertRecentCallUpdate,
  upsertAiCallInsight,
  upsertGeneratedAgendaAppointment,
  queueRuntimeStatePersist,
  shared,
}) {
  return buildServerAppOpsWiringContext({
    appOpsOptions: {
      app,
      env,
      isSupabaseConfigured: platformRuntime.isSupabaseConfigured,
      runtimeStateSyncState: runtimeMemory.runtimeStateSyncState,
      supabaseStateTable: envConfig.SUPABASE_STATE_TABLE,
      supabaseStateKey: envConfig.SUPABASE_STATE_KEY,
      isSmtpMailConfigured,
      isImapMailConfigured,
      mailImapMailbox: envConfig.MAIL_IMAP_MAILBOX,
      mailImapPollCooldownMs: envConfig.MAIL_IMAP_POLL_COOLDOWN_MS,
      confirmationMailRuntimeState: runtimeMemory.confirmationMailRuntimeState,
      getColdcallingProvider: platformRuntime.getColdcallingProvider,
      normalizeString: shared.normalizeString,
      getMissingEnvVars: platformRuntime.getMissingEnvVars,
      premiumSessionSecret: envConfig.PREMIUM_SESSION_SECRET,
      premiumSessionCookieName: envConfig.PREMIUM_SESSION_COOKIE_NAME,
      isPremiumMfaConfigured: securityRuntime.isPremiumMfaConfigured,
      recentWebhookEvents: runtimeMemory.recentWebhookEvents,
      recentCallUpdates: runtimeMemory.recentCallUpdates,
      recentAiCallInsights: runtimeMemory.recentAiCallInsights,
      recentSecurityAuditEvents: runtimeMemory.recentSecurityAuditEvents,
      generatedAgendaAppointments: runtimeMemory.generatedAgendaAppointments,
      appName: 'softora-retell-coldcalling-backend',
      appVersion,
      featureFlags,
      getPublicFeatureFlags,
      routeManifest,
      requireRuntimeDebugAccess: securityRuntime.requireRuntimeDebugAccess,
      buildRuntimeStateSnapshotPayloadWithLimits:
        runtimeSyncRuntime.buildRuntimeStateSnapshotPayloadWithLimits,
      buildRuntimeBackupForOps: runtimeSyncRuntime.buildRuntimeBackupForOps,
      isProduction: envConfig.IS_PRODUCTION,
      isServerlessRuntime,
      assetsDirectory: `${projectRootDir}/assets`,
      securityContactEmail: envConfig.SECURITY_CONTACT_EMAIL,
      getEffectivePublicBaseUrl,
      sendSeoManagedHtmlPageResponse: uiSeoRuntime.sendSeoManagedHtmlPageResponse,
      resolveLegacyPrettyPageRedirect,
      toPrettyPagePathFromHtmlFile,
      knownHtmlPageFiles: bootstrapState.knownHtmlPageFiles,
      knownPrettyPageSlugToFile: bootstrapState.knownPrettyPageSlugToFile,
      sendPublishedWebsiteLinkResponse: (req, res, slug) =>
        websiteLinkCoordinator.sendPublishedWebsiteLinkResponse(req, res, slug),
      demoConfirmationTaskEnabled: envConfig.DEMO_CONFIRMATION_TASK_ENABLED,
      upsertRecentCallUpdate,
      upsertAiCallInsight,
      upsertGeneratedAgendaAppointment,
      queueRuntimeStatePersist,
      ensureRuntimeStateHydratedFromSupabase:
        runtimeSyncRuntime.ensureRuntimeStateHydratedFromSupabase,
      log: (message) => console.log(message),
    },
  });
}

