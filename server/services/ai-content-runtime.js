const { createAiSummaryService } = require('./ai-summary');
const { createOrderDossierHelpers } = require('./order-dossier');
const { createAiRemoteService } = require('./ai-remote');

function createAiContentRuntime(deps = {}) {
  const {
    normalizeString,
    truncateText,
    parseIntSafe,
    fetchJsonWithTimeout,
    getOpenAiApiKey,
    extractOpenAiTextContent,
    openAiApiBaseUrl,
    openAiModel,
    clipText,
    escapeHtml,
    env,
    getAnthropicApiKey,
    getWebsiteGenerationProvider,
    getWebsiteAnthropicModel,
    getDossierAnthropicModel,
    getAnthropicDossierMaxTokens,
    fetchTextWithTimeout,
    extractAnthropicTextContent,
    parseJsonLoose,
    assertWebsitePreviewUrlIsPublic,
    normalizeWebsitePreviewTargetUrl,
    extractWebsitePreviewScanFromHtml,
    buildWebsitePreviewPromptFromScan,
    buildWebsitePreviewBriefFromScan,
    buildWebsitePreviewDownloadFileName,
    buildWebsiteGenerationPrompts,
    ensureHtmlDocument,
    ensureStrictAnthropicHtml,
    isLikelyUsableWebsiteHtml,
    buildLocalWebsiteBlueprint,
    buildAnthropicWebsiteHtmlPrompts,
    getAnthropicWebsiteStageEffort,
    getAnthropicWebsiteStageMaxTokens,
    supportsAnthropicAdaptiveThinking,
    sanitizeReferenceImages,
    parseImageDataUrl,
    estimateOpenAiUsageCost,
    estimateOpenAiTextCost,
    estimateAnthropicUsageCost,
    estimateAnthropicTextCost,
    openAiImageModel,
    anthropicApiBaseUrl,
    anthropicModel,
    websiteGenerationTimeoutMs,
    websiteGenerationStrictAnthropic,
    websiteGenerationStrictHtml,
  } = deps;

  const aiSummaryService = createAiSummaryService({
    normalizeString,
    truncateText,
    parseIntSafe,
    fetchJsonWithTimeout,
    getOpenAiApiKey,
    extractOpenAiTextContent,
    openAiApiBaseUrl,
    openAiModel,
  });

  const {
    buildOrderDossierInput,
    buildOrderDossierFallbackLayout,
    buildAnthropicOrderDossierPrompts,
    normalizeOrderDossierLayout,
  } = createOrderDossierHelpers({
    parseIntSafe,
    normalizeString,
    clipText,
    escapeHtml,
  });

  const aiRemoteService = createAiRemoteService({
    env,
    normalizeString,
    truncateText,
    getOpenAiApiKey,
    getAnthropicApiKey,
    getWebsiteGenerationProvider,
    getWebsiteAnthropicModel,
    getDossierAnthropicModel,
    getAnthropicDossierMaxTokens,
    fetchJsonWithTimeout,
    fetchTextWithTimeout,
    extractOpenAiTextContent,
    extractAnthropicTextContent,
    parseJsonLoose,
    assertWebsitePreviewUrlIsPublic,
    normalizeWebsitePreviewTargetUrl,
    extractWebsitePreviewScanFromHtml,
    buildWebsitePreviewPromptFromScan,
    buildWebsitePreviewBriefFromScan,
    buildWebsitePreviewDownloadFileName,
    buildWebsiteGenerationPrompts,
    ensureHtmlDocument,
    ensureStrictAnthropicHtml,
    isLikelyUsableWebsiteHtml,
    buildLocalWebsiteBlueprint,
    buildAnthropicWebsiteHtmlPrompts,
    getAnthropicWebsiteStageEffort,
    getAnthropicWebsiteStageMaxTokens,
    supportsAnthropicAdaptiveThinking,
    sanitizeReferenceImages,
    parseImageDataUrl,
    estimateOpenAiUsageCost,
    estimateOpenAiTextCost,
    estimateAnthropicUsageCost,
    estimateAnthropicTextCost,
    buildAnthropicOrderDossierPrompts,
    normalizeOrderDossierLayout,
    openAiApiBaseUrl,
    openAiModel,
    openAiImageModel,
    anthropicApiBaseUrl,
    anthropicModel,
    websiteGenerationTimeoutMs,
    websiteGenerationStrictAnthropic,
    websiteGenerationStrictHtml,
  });

  const {
    buildWebsitePromptFallback,
    extractMeetingNotesFromImageWithAi,
    fetchWebsitePreviewScanFromUrl,
    generateDynamicOrderDossierWithAnthropic,
    generateWebsiteHtmlWithAi,
    generateWebsitePreviewImageWithAi,
    generateWebsitePromptFromTranscriptWithAi,
  } = aiRemoteService;

  return {
    aiSummaryService,
    buildOrderDossierFallbackLayout,
    buildOrderDossierInput,
    buildWebsitePromptFallback,
    extractMeetingNotesFromImageWithAi,
    fetchWebsitePreviewScanFromUrl,
    generateDynamicOrderDossierWithAnthropic,
    generateWebsiteHtmlWithAi,
    generateWebsitePreviewImageWithAi,
    generateWebsitePromptFromTranscriptWithAi,
  };
}

module.exports = {
  createAiContentRuntime,
};
